  checksum      = '4288512b9005a846fde4242f0977df8359501cc426ddcdd9e61225e36143eb23'
  checksumType  = 'sha256'

  silentArgs    = "/params:/silent"
  validExitCodes= @(0)
}

Install-ChocolateyPackage @packageArgs
